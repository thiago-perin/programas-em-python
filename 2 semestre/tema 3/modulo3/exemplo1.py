a=5
b=2
print('a =',a)
print('b =',b)
# + adição
print(a,'+',b,'=',a+b)
# - subtração
print(a,'-',b,'=',a-b)
# * multiplicação
print(a,'*',b,'=',a*b)
# / divisão
print(a,'/',b,'=',a/b)
# ** potencia
print(a,'**',b,'=',a**b)
# // divisão inteira
print(a,'//',b,'=',a//b)
# % resto da divisão
print(a,'%',b,'=',a%b)
